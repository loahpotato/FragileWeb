package cn.fragile.pojo;

public class Cart {
    public int cart_id;
    public String cart_pimage;
    public String cart_pname;
    public int cart_pprice;
    public int cart_uid;
    public int cart_valid;
    public int cart_pid;

    public int getCart_id() {
        return cart_id;
    }

    public void setCart_id(int cart_id) {
        this.cart_id = cart_id;
    }

    public String getCart_pimage() { return cart_pimage;  }

    public void setCart_pimage(String cart_pimage) { this.cart_pimage = cart_pimage; }

    public String getCart_pname() {
        return cart_pname;
    }

    public void setCart_pname(String cart_pname) {
        this.cart_pname = cart_pname;
    }

    public int getCart_pprice() {
        return cart_pprice;
    }

    public void setCart_pprice(int cart_pprice) {
        this.cart_pprice = cart_pprice;
    }

    public int getCart_uid() {
        return cart_uid;
    }

    public void setCart_uid(int cart_uid) {
        this.cart_uid = cart_uid;
    }

    public int getCart_valid() {
        return cart_valid;
    }

    public void setCart_valid(int cart_valid) {
        this.cart_valid = cart_valid;
    }

    public int getCart_pid() { return cart_pid; }

    public void setCart_pid(int cart_pid) { this.cart_pid = cart_pid; }
}
